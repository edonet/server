'use strict';


/**
 *****************************************
 * 加载依赖
 *****************************************
 */
const yargs = require('yargs');


/**
 *****************************************
 * 定义参数
 *****************************************
 */
yargs
    .boolean('keepalived')
    .alias('k', 'keepalived');


/**
 *****************************************
 * 加载脚本
 *****************************************
 */
require(yargs.argv.keepalived ? './app' : './start');
