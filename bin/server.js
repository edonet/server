'use strict';


/**
 *****************************************
 * 加载依赖
 *****************************************
 */
const yargs = require('yargs');


/**
 *****************************************
 * 定义参数
 *****************************************
 */
yargs
    .boolean('keepalived')
    .alias('k', 'keepalived');


/**
 *****************************************
 * 加载脚本
 *****************************************
 */
if (yargs.argv.keepalived) {
    require('../lib')().catch(console.error);
} else {
    require('./start');
}
